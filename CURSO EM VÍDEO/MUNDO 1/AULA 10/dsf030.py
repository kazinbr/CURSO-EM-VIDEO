'''
Crie um programa que leia um número inteiro e mostre
na tela se ele é par ou impar.
'''
print('Vamos brincar? Vou descobrir se seu número é ímpar ou par!!')
n1 = int(input('Escolha um número: '))
if n1 % 2 == 0:
    print(f'Já sei!! O número {n1} é par!!')
else:
    print(f'Já sei!! O número {n1} é ímpar!!')
